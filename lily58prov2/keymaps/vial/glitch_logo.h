/*
 * glitch_logo.h — animated OLED glitch effect for QMK
 *
 * Usage:
 *   1. Place this file next to your keymap.c
 *   2. Add  #include "glitch_logo.h"  inside your #ifdef OLED_ENABLE block
 *      (after #include QMK_KEYBOARD_H has already been seen)
 *   3. Call  render_glitch_logo();  from your oled_task_user()
 *
 * To use on another keyboard, copy this file, replace the khor_logo[] bytes
 * with your own 128×32 bitmap (512 bytes, SSD1306 page-column order), and
 * adjust GLITCH_LOGO_SIZE if your display is a different resolution.
 *
 * Tuning constants (edit to taste):
 *   GLITCH_FRAME_MS   – duration of each glitch frame in milliseconds
 *   GLITCH_MIN_FRAMES – minimum frames per burst
 *   GLITCH_MAX_FRAMES – maximum frames per burst
 *   GLITCH_MIN_IDLE   – minimum idle time between bursts (ms)
 *   GLITCH_MAX_IDLE   – maximum idle time between bursts (ms)
 *   GLITCH_MAX_SHIFT  – maximum column shift for row-shift effect
 */

