/* ── Glitch animation ──────────────────────────────────────────────────── */
static void render_glitch_logo(void) {
    static uint16_t prng        = 0;
    static uint32_t idle_timer  = 0;
    static uint32_t frame_timer = 0;
    static uint8_t  frames_left = 0;
    static uint32_t next_glitch = GLITCH_MIN_IDLE;

    /* Seed PRNG from boot timer on first call */
    if (!prng) prng = (uint16_t)(timer_read32() | 1);

    /* Mutable RAM copy — static so it lives in .bss, not on the stack */
    static uint8_t gbuf[GLITCH_LOGO_SIZE];
    memcpy_P(gbuf, khor_logo, GLITCH_LOGO_SIZE);

    /* ── Idle: wait for the next burst ──────────────────────────────── */
    if (frames_left == 0) {
        if (timer_elapsed32(idle_timer) >= next_glitch) {
            uint8_t span = GLITCH_MAX_FRAMES - GLITCH_MIN_FRAMES + 1;
            frames_left  = GLITCH_MIN_FRAMES + (uint8_t)(_GRAND(prng) % span);
            uint32_t range = GLITCH_MAX_IDLE - GLITCH_MIN_IDLE;
            next_glitch  = GLITCH_MIN_IDLE + (uint32_t)((_GRAND(prng) & 0xFF) * (range / 255));
            frame_timer  = timer_read32();
        }
    }

    /* ── Glitching: corrupt the buffer then write it ─────────────────── */
    if (frames_left > 0) {
        uint8_t pages = GLITCH_LOGO_SIZE / 128; /* number of 8-px-tall pages */

        /* Effect A – horizontal row shift (every frame, most iconic) */
        {
            uint8_t  page  = (uint8_t)(_GRAND(prng) % pages);
            int8_t   shift = (int8_t)(_GRAND(prng) % (GLITCH_MAX_SHIFT * 2 + 1))
                             - GLITCH_MAX_SHIFT;
            uint8_t *p     = gbuf + (uint16_t)page * 128;
            if (shift > 0) {
                uint8_t s = (uint8_t)shift;
                memmove(p + s, p, 128 - s);
                memset(p, 0x00, s);
            } else if (shift < 0) {
                uint8_t s = (uint8_t)(-shift);
                memmove(p, p + s, 128 - s);
                memset(p + 128 - s, 0x00, s);
            }
        }

        /* Effect B – inversion stripe (~50 % chance per frame) */
        if (_GRAND(prng) & 0x01) {
            uint8_t  page  = (uint8_t)(_GRAND(prng) % pages);
            uint8_t  start = (uint8_t)(_GRAND(prng) % 90);
            uint8_t  width = 6 + (uint8_t)(_GRAND(prng) % 22);
            uint8_t *base  = gbuf + (uint16_t)page * 128;
            for (uint8_t i = start; i < start + width && i < 128; i++) {
                base[i] ^= 0xFF;
            }
        }

        /* Effect C – full scanline dropout (~12 % chance per frame) */
        if ((_GRAND(prng) & 0x07) == 0) {
            uint8_t page = (uint8_t)(_GRAND(prng) % pages);
            memset(gbuf + (uint16_t)page * 128, 0x00, 128);
        }

        /* Advance frame */
        if (timer_elapsed32(frame_timer) >= GLITCH_FRAME_MS) {
            frame_timer = timer_read32();
            if (--frames_left == 0) idle_timer = timer_read32();
        }
    }

    oled_write_raw((const char *)gbuf, GLITCH_LOGO_SIZE);
}

