/*
 * status_display.h — WPM counter + layer indicator + Luna pet for QMK
 *
 * Usage:
 *   1. Place this file next to your keymap.c
 *   2. Add  #include "status_display.h"  inside your #ifdef OLED_ENABLE block
 *      (after QMK_KEYBOARD_H has been included)
 *   3. Call  render_status_display();  from oled_task_user() on the slave side
 *
 * Optional — hook up the pet triggers in process_record_user():
 *
 *     bool process_record_user(uint16_t keycode, keyrecord_t *record) {
 *         switch (keycode) {
 *             case KC_LSFT: case KC_RSFT:
 *                 pet_is_sneaking = record->event.pressed; break;
 *             case KC_SPC:
 *                 if (record->event.pressed) pet_is_jumping = true; break;
 *         }
 *         return true;
 *     }
 */

