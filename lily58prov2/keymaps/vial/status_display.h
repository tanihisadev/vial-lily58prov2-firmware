/* ── Internal: draw one frame of the pet at (x, y) ────────────────────── */
static void _pet_draw_frame(int x, int y) {
    uint8_t wpm = get_current_wpm();

    /* Handle jump position */
    if (pet_is_jumping || !_pet_showed_jump) {
        oled_set_cursor(x, y + 2);
        oled_write("     ", false);
        oled_set_cursor(x, y - 1);
        _pet_showed_jump = true;
    } else {
        oled_set_cursor(x, y - 1);
        oled_write("     ", false);
        oled_set_cursor(x, y);
    }

    /* Advance frame (0 / 1 alternating) */
    _pet_frame = (_pet_frame + 1) % 2;

    /* Pick sprite based on state */
    if (host_keyboard_led_state().caps_lock) {
        oled_write_raw_P(_pet_bark[_pet_frame],  _PET_SPRITE_BYTES);
    } else if (pet_is_sneaking) {
        oled_write_raw_P(_pet_sneak[_pet_frame], _PET_SPRITE_BYTES);
    } else if (wpm <= PET_MIN_WALK_WPM) {
        oled_write_raw_P(_pet_sit[_pet_frame],   _PET_SPRITE_BYTES);
    } else if (wpm <= PET_MIN_RUN_WPM) {
        oled_write_raw_P(_pet_walk[_pet_frame],  _PET_SPRITE_BYTES);
    } else {
        oled_write_raw_P(_pet_run[_pet_frame],   _PET_SPRITE_BYTES);
    }

    /* Clear jumping flag after one frame */
    if (pet_is_jumping) {
        pet_is_jumping   = false;
        _pet_showed_jump = false;
    }
}

/* ── Public: drive the pet animation ──────────────────────────────────── */
static void render_pet(int x, int y) {
#if OLED_TIMEOUT > 0
    if (last_input_activity_elapsed() > OLED_TIMEOUT &&
        last_led_activity_elapsed()   > OLED_TIMEOUT) {
        oled_off();
        return;
    } else {
        oled_on();
    }
#endif
    if (timer_elapsed32(_pet_timer) > PET_FRAME_MS) {
        _pet_timer = timer_read32();
        _pet_draw_frame(x, y);
    }
}

/* ── Public: full right-side status panel ─────────────────────────────── */
/*
 * Layout (32-col OLED rotated 270°, i.e. 5 chars wide × 16 rows tall):
 *
 *   LAYER        ← label
 *   BASE         ← current layer name
 *   _____
 *   CAP:O/X      ← caps-lock indicator
 *    WPM
 *    ###          ← 3-digit WPM
 *   _____
 *   [Luna]       ← animated pet (rows 13-15)
 */
static void render_status_display(void) {
    /* Layer */
    oled_write_P(PSTR("LAYER\n"), false);
    switch (get_highest_layer(layer_state)) {
        case 0:  oled_write_ln_P(PSTR("BASE "), false); break;
        case 1:  oled_write_ln_P(PSTR("LOWER"), false); break;
        case 2:  oled_write_ln_P(PSTR("RAISE"), false); break;
        default: oled_write_ln_P(PSTR("?????"), false); break;
    }

    /* Divider */
    oled_write_P(PSTR("_____\n"), false);

    /* Caps Lock */
    oled_write_P(PSTR("CAP:"), false);
    oled_write_P(host_keyboard_led_state().caps_lock ? PSTR("O\n") : PSTR("X\n"), false);

    /* WPM */
    uint8_t n = get_current_wpm();
    char wpm_str[4];
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write_P(PSTR(" WPM\n"), false);
    oled_write_P(PSTR(" "), false);
    oled_write(wpm_str, false);

    /* Divider + pet */
    oled_write_P(PSTR("\n_____"), false);
    render_pet(0, 13);
}
